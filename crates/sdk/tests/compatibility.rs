fn push_encoding_cases(
    context: &Context,
    cases: &mut Vec<CompatibilityCase>,
) -> Result<(), Box<dyn Error>> {
    let export_value = context.compile_source("export.cue", "x: 1\n")?;
    let mut options = EncodeOptions::default();
    options.encoding = Encoding::Json;
    cases.push(supported_case(
        "export/json-object",
        "encoding",
        encode_value(&export_value, options)?.contains("\"x\": 1"),
    ));
    Ok(())
}

fn push_security_cases(cases: &mut Vec<CompatibilityCase>) {
    let mut limited_decode = DecodeOptions::default();
    limited_decode.max_depth = 0;
    cases.push(supported_case(
        "decode/depth-limit",
        "security",
        decode_bytes(Encoding::Json, br#"{"x":1}"#, limited_decode).is_err(),
    ));
}

async fn push_known_gap_cases(
    context: &Context,
    cases: &mut Vec<CompatibilityCase>,
) -> Result<(), Box<dyn Error>> {
    cases.push(supported_case(
        "compile/unsupported-import-diagnostic",
        "loader-gap",
        context
            .compile_source("import.cue", "import \"example.com/remote/pkg\"\nx: 1\n")
            .is_err(),
    ));

    let loader_passed = local_import_registry_case(context)
        .await
        .is_ok_and(|passed| passed);
    cases.push(supported_case(
        "loader/import-registry",
        "loader-gap",
        loader_passed,
    ));
    push_phase9_parity_cases(context, cases);
    cases.push(expected_gap_case(
        "eval/upstream-cycle-diagnostics",
        "semantic-gap",
        "inline and top-level structural/list cycles now resolve for the currently covered vendor \
         cases, and disjunction/default fixpoint cycles are covered by supported parity cases; \
         remaining upstream parity work is broader cycle-diagnostic coverage and exact diagnostic \
         shaping",
    ));
    cases.push(expected_gap_case(
        "stdlib/math-pow-fractional-decimal",
        "stdlib-gap",
        "math.Pow now covers exact integer exponents, including finite negative-exponent \
         decimals; fractional exponents still require APD-compatible decimal transcendental \
         semantics",
    ));
    Ok(())
}

async fn local_import_registry_case(context: &Context) -> Result<bool, Box<dyn Error>> {
    let root = fixture_dir().await?;
    let import_dir = root.join("cue.mod/pkg/example.com/lib");
    let dep_dir = root.join("cue.mod/pkg/example.com/dep");
    fs::create_dir_all(&import_dir).await?;
    fs::create_dir_all(&dep_dir).await?;
    fs::write(
        root.join("main.cue"),
        "package p\nimport \"example.com/lib\"\nout: lib.value\n",
    )
    .await?;
    fs::write(
        import_dir.join("lib.cue"),
        "package lib\nimport \"example.com/dep\"\nvalue: dep.value + 1\n",
    )
    .await?;
    fs::write(dep_dir.join("dep.cue"), "package dep\nvalue: 2\n").await?;
    let instances = context
        .load(
            LoadConfig::builder()
                .current_dir(Some(root.clone()))
                .module_root(Some(root))
                .build(),
            &[PathBuf::from(".").try_into()?],
        )
        .await?;
    let Some(instance) = instances.first() else {
        return Ok(false);
    };
    let value = context.build_instance(instance)?;
    Ok(
        value.lookup_path(&["out"])?.evaluate()?
            == cue_rust::EvaluatedValue::Number("3".to_owned()),
    )
}

